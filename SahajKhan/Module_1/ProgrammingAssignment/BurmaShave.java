import javax.swing.*;

/**
 * @author Sahaj Khan
 * ind a classic Burma Shave rhyme on the Web. Write, compile, and test a class that produces a series of four dialog
 * boxes so that each displays one line of a Burma Shave slogan in turn.
 * @version 1.0
 */
public class BurmaShave {
    public static void main(String[] args) {
        System.out.println("He lit a match");
        System.out.println("To check gas tank");
        System.out.println("That's why");
        System.out.println("They call him");
        System.out.println("Skinless Frank");

        JOptionPane.showMessageDialog(null, "He lit a match");
        JOptionPane.showMessageDialog(null, "To check gas tank");
        JOptionPane.showMessageDialog(null, "That's why");
        JOptionPane.showMessageDialog(null, "They call him");
        JOptionPane.showMessageDialog(null, "Skinless Frank");
    }
}
